// difference between fs.exists and fs.existsSync
// fs.existsSync is synchronous, fs.exists is asynchronous

// fs.existsSync returns true or false, fs.exists returns a callback with true or false

// fs.existsSync is more modern and widely supported, fs.exists is deprecated

// fs.existsSync is more efficient, fs.existsSync is more readable

// fs.existsSync is more consistent with other Node.js APIs, fs.exists is inconsistent with other Node.js